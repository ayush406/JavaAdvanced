package com.company;

public class loops {

    public static void main(String[] args) {

        int i =0;
        while(i<5)
        {
            System.out.println(i);
            i++;
        }
        int b= 10;
        do{
            System.out.println(b);
            b++;
        }while (b<5);

        for(char a = 'A'; a<'D'; a++)
        {
            System.out.println(a);
        }
    }
}
