package com.company;

public class WhileLoopDoubts {

    public static void main(String[] args) {

        int i,j;
        i=0;
        j=0;

        while(i<4)
        {
            System.out.println(++i);
        }
        while(j<5)
        {
            System.out.println(j++);
        }



    }
}
